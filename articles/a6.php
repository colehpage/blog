				<p>Among other telling statistics in the defensive liability that is early season 2017 LeBron James is Opponent Points off Turnovers (effort, effort, effort!) where he ranks 429th out of 430 players, only 3 points behind defensive virtuoso James Harden. Add that to 414/430 for Opp 2nd Chance points, 410/430 in Opp Fast Break Points, and 428/430 in Opp Points in the Paint and you get a league bottom 5 worst <a href="https://stats.nba.com/players/defense/?sort=DEF_WS&dir=1&Season=2017-18&SeasonType=Regular%20Season&PerMode=Totals&CF=PLAYER_NAME*E*&DateTo=11%2F10%2F2017">Defensive Win Share of -0.2</a>, yes that's NEGATIVE as in "the King" personally detracts from the leagues already worst defense. Granted his fellow Cavaliers JR Smith, Kevin Love, and Derrick Rose also round out the top 5 (with Dallas' Wesley Matthews) but you expect the best player on the planet to at least not make things worse. I mean, no disrespect to Taurean Prince but you're LeBron James, there is 30seconds left in the quarter, you are losing to the Atlanta Hawks and because of your "unreal basketball IQ" you know they are going for the 2 for 1 and you just do everything in your power to let them....</p>

