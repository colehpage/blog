				<p>I might be a hater, but I'm not a total asshole. The Cavs are only 12 games into a season where they brought in 10 new players, lost 8, and only kept 7 so a little adjustment time is warranted, but waiting on IT's return to fix this Defense is not going to cut it. If things are going to change, and I truley think they will, LeBron needs to <u>put forth some effort</u>, plain and simple.</p>


