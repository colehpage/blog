			<p>Just prior to the start of the 2016-2017 NBA season when the big-man depleted Golden State Warriors signed JaVale Magee to a 1-year minimum contract, the critics quickly surfaced. Articles bashing JaVale we’re numerous, and praise nonexistent. An article on Complex ranked JaVale as the 12th worst player in the NBA and wrote “The Golden State Warriors need help defending the paint so they’d figure, ‘Hey, JaVale is a giant human with a heartbeat, let’s sign him!’ Problem is he really isn’t that good. Maybe the organization of the future will somehow turn JaVale into an elite big man, or maybe they’re just desperate for some rim protection.”</p>

			<p>Well, seven months later, four wins into the 2017 playoffs, it looks like the Golden State organization may have done exactly that. Not only did JaVale produce important big-man minutes off the bench all season, he started ten games in place of the injured Zaza Pachulia and put together one of the most impressive per-minute statistical seasons for a center in recent memory. JaVale only averaged 9.6 minutes a game and much of his statistical dominance may be attributed to the strength of the league-leading team surrounding him, but anyone who actually watched this season got a chance to a see a whole new JaVale: a powerfully athletic, rim destroying, ball swatting monster having a career year. </p>

			<p>Looking at his raw averages of 6.1 points, 3.2 rebounds, and 0.9 blocks in only 9.6 minutes per game won’t blow you away, but his per-36-minute stats are absurd. In this article we take a look at just how impressive a starting full-minutes JaVale would rank up against the rest of the league. I’m not suggesting that increasing his play time would effect his stats linearly like this, and with Golden State’s depth and small-ball play we most likely aren’t going to find out any time soon, but the Warriors are without a doubt playing some of their best basketball when he's on the floor, and so far JaVale has silenced most if not all of his critics; looking at you Shaq.</p>



			<h2>Shooting</h2>
			<h3>Dunks</h3>

			<p>Of all the stats I am going to talk about today, this one should be the least surprising and is without a doubt the most dominant. Through all his blunders and mistakes over the years, JaVale has never failed to thrown down with authority. At 7’ tall and sporting an 8’ wingspan, there aren’t many people on the planet who have that kind of range around the rim. Hometown bias and NBA sponsorship (KIA) aside, JaVale probably should have won the 2011 Slam Dunk Contest, and annually puts up highlight reel dunks. With a fraction of the minutes played, JaVale still ranked 12th in total dunks this season. Adjusted, JaVale is averaging 5.89 blocks/36min, a whopping 2.35 more than this seasons dunk leader DeAndre Jordan. The Clippers might still hold the title of Lob City but nobody has dominated the Ally-Oop this season like Draymond, Andre, and Steph chucking up bombs to JaVale McGee.</p>


