			<h2>Rebounding</h2>
			<p>JaVale’s defensive rebound statistics are impressive for a team that defaults it’s rebounds to pace pushing forwards like Green, Iguadala, and Durant; similar to why we don’t see Houston or Oklahoma City bigs on this list. What really stands out is his adjusted OREB per-36, which would sit at #2 in the league at 4.87 if he played starters minutes. Many of the statistics we discussed above are due to offensive rebounding. JaVale doesn’t come into the game expecting to be integral in the GS offensive aside from lobs to the rim, instead taking advantage of the top perimeter shooting offensive in the league and the way it pulls opposing defenders to the 3pt line. The Warriors hit quite a lot of their long balls, but when they don’t, JaVale has been there to pull down boards and put them back in with authority. This consistent combination of dominant physical athleticism and mental awareness is a JaVale the league hasn’t seen before. </p>
